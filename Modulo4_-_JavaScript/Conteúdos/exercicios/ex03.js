function printNumber(number){
    switch (number){
        case 1:
            console.log("numero 1");
            break
        case 2:
            console.log("numero 2");
            break
        case 3:
            console.log('numero 3')
            break
        case 4:
            console.log('numero 3')
            break
        default:
            console.log('this is default')
    }
}

